2. Explicar:

Backup full: copia completa de la BD, la cual incluye tablas, indices, datos, esquema, configuraciones.

Backup incremental: guarda solo los cambios realizados desde el ultimo backup.

Backup diferencial: guarda el respaldod esde el ultimo backul full realizado.


3. ¿Qué harías ante corrupción de base de datos?

Identificar el tipo de corrupción.
Tomar acción inmediata según el protocolo establecido, como por ejemplo restringir el acceso a la BD.
Realiza el backup.
Realizar el diagnostico.
Realizar las acciones pertinentes si no es corrupción completa de la BD.
Reastauras desde el backup.
Realizar plan de prevención.